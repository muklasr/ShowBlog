     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        Article::updateOrCreate(
            ['id' => $request->id],
            ['name' => $request->name,'content' => $request->content,'category_id' => $request->category_id]
        );
        return redirect("/article")->with('success', 'Article saved successfully');
